// src/cost/threshold.js
// Порог автоматического продолжения без подтверждения пользователя — см.
// «07. Архитектура (Бекенд).md», §8.3. $0.50, выше, чем у Агента 3 ($0.10),
// т.к. генерация контента по своей природе дороже парсинга/анализа.
//
// Применимо прежде всего к генерации с известной заранее ценой за операцию
// (видео/изображения — Слайсы 4-5, тарифы известны до вызова). Для текста
// (Слайс 2) реальная стоимость через OpenRouter обычно на 2-3 порядка ниже
// порога и известна только постфактум — pre-flight оценка тут неприменима,
// функция всё равно нужна как общий строительный блок для будущих слайсов.
const DEFAULT_THRESHOLD_USD = 0.5;

export function requiresConfirmation(estimatedCostUsd, threshold = DEFAULT_THRESHOLD_USD) {
  return estimatedCostUsd > threshold;
}
